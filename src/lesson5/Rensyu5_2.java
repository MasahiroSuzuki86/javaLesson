package lesson5;

public class Rensyu5_2 {

	/**
	 * 実行メソッド
	 * @param args
	 */
	public static void main(String[] args) {
		System.out.println("メソッドを呼び出します");
		hello();
		System.out.println("メソッドの呼び出しが終わりました");
	}

	/**
	 * メッセージを表示するメソッド
	 */
	public static void hello() {
		System.out.println("湊さん、こんにちは");
	}

}
